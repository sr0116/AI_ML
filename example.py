import numpy as np
import pandas as pd
import matplotlib.pyplot as plt


if __name__ == "__main__":
    # ----------------------------------------------------
    # C : CREATE (데이터 생성)
    # DataFrame을 딕셔너리로 생성
    # ----------------------------------------------------
    df = pd.DataFrame({
        "국어": [80, 90, 85],
        "영어": [70, 95, 88]
    })

    # ----------------------------------------------------
    # R : READ (조회)
    # ----------------------------------------------------

    # 전체 조회
    print("=== 전체 테이블 ===")
    print(df)

    # 단일 컬럼 조회 (Projection)
    print("=== 국어 컬럼 ===")
    print(df["국어"])

    # 조건 조회 (WHERE 절과 동일)
    print("=== 국어 점수가 90 이상인 행 ===")
    print(df[df["국어"] >= 90])

    # 여러 컬럼 조회
    print("=== 국어, 영어 컬럼만 조회 ===")
    print(df[["국어", "영어"]])

    # ----------------------------------------------------
    # U : UPDATE (값 수정)
    # ----------------------------------------------------

    # loc 사용 - 존재하지 않는 index "a"를 사용하면 새로운 행 생성됨
    # SQL 기준으로 보면 INSERT와 UPDATE 성격을 동시에 가진다.
    df.loc["a", "국어"] = 100
    print("=== 행 추가 또는 수정 (index a 생성) ===")
    print(df)

    # ----------------------------------------------------
    # D : DELETE (삭제)
    # ----------------------------------------------------

    # 컬럼 삭제(drop)
    # inplace=True → 원본 df를 직접 수정
    # 반환값은 None → new_df 에는 아무 것도 들어가지 않는다.
    print("=== 영어 컬럼 삭제 (inplace=True) ===")
    new_df = df.drop("영어", axis=1, inplace=True)
    print("new_df 값:", new_df)  # None 출력
    print("df 상태 변경됨:")
    print(df)
